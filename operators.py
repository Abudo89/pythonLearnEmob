# arithmetic operators
a = 9
b = 11

print(a + b)  # add
print(a - b)  # sub
print(a * b)  # multiply
print(a / b)  # division
print(a % b)  # remainder

# comparison operator
c = 56
d = 21

print(f" {c} is equal to {b} {c == b}")
print(f" {c} is not equal to {b} {c != b}")
print(f" {c} is greater than {d} {c > d}")
print(f" {c} is greater than or equal to {d} {c >= d}")
print(f" {c} is less than  {d} {c < d}")
print(f" {c} is less than or equal to   {d} {c <= d}")

# logical operators
e = 11
print(e > 3 and e > 11)
print(e > 3 or e < 11)
